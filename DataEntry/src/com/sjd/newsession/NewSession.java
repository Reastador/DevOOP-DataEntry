package com.sjd.newsession;


public class NewSession {
	
	private long time;
	private String id;
	private int session;
	
	public long getTime() {
		return time;
	}
	public void setTime(long l) {	
		this.time = l;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		
		this.id = id;
	}
	
	public int getSession() {
		return session;
	}
	public void setSession(int session) {
		this.session = session;
	}
	
	
	
}
